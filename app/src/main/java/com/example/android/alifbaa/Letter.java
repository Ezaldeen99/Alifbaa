public class Letter {
    private int indext;
    private int letterImg;

    public Letter(int indext, int letterImg) {
        this.indext = indext;
        this.letterImg = letterImg;
    }

    public int getIndext() {
        return indext;
    }

    public int getLetterImg() {
        return letterImg;
    }
}
